#include <unistd.h>
int	main(int arc, char **arv)
{
	int	i;
	
	i = 0;
	while(arv[0][i] != '\0')
	{
		write(1, &arv[0][i], 1);
		i++;
	}
	write(1, "\n", 1);
	return (0);
}
